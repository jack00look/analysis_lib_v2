    def run_analysis(self):
        """Run analysis on all datasets"""
        logger.info(f"\n{'='*80}")
        logger.info(f"KZ ARTICLE ANALYSIS - Iteration {self.iteration}")
        logger.info(f"{'='*80}\n")
        
        dataset_results = {}
        
        for dataset_idx, dataset_config in enumerate(self.config['datasets']):
            dataset_id = dataset_config['dataset_id']
            logger.info(f"\n{'='*80}")
            logger.info(f"DATASET: {dataset_id}")
            logger.info(f"{'='*80}")
            
            # For first dataset, prompt to preprocess if needed
            if dataset_idx == 0:
                self._prompt_preprocessing_workflow(dataset_config)
            
            # Step 1: Show sample profiles
            logger.info("Step 1: Showing sample profiles for affine coefficient tuning...")
            result = self.show_sample_profiles(dataset_config)
            
            if result[0] is None:
                logger.error(f"Skipping {dataset_id}")
                continue
            
            fig, df_filtered = result
            
            plt.show(block=False)
            
            # Step 2: Prompt for affine correction
            logger.info("\nStep 2: Adjust affine correction coefficients")
            affine = self.prompt_affine_correction(dataset_config['affine_correction'])
            dataset_config['affine_correction'] = affine
            plt.close(fig)
            
            # Step 3: Prompt for integration limits
            logger.info("\nStep 3: Adjust integration limits")
            x_min, x_max = self.prompt_integration_limits(
                dataset_config['x_min_integration'],
                dataset_config['x_max_integration']
            )
            dataset_config['x_min_integration'] = x_min
            dataset_config['x_max_integration'] = x_max
            
            # Step 4: Run waterfall analysis (KZ_det_scan mode)
            logger.info("\nStep 4: Running waterfall analysis (KZ_det_scan mode)...")
            # TODO: Implement waterfall_plot call with KZ_det_scan mode
            logger.info("Waterfall analysis would run here with updated parameters")
            
            # Step 5: Prompt for domain fit window
            logger.info("\nStep 5: Adjust domain wall fit window")
            dw_window = self.prompt_domain_fit_window(dataset_config['domain_balance_fit_window'])
            dataset_config['domain_balance_fit_window'] = dw_window
            
            # Step 6: Ask for permission to continue
            print("\n" + "="*80)
            while True:
                response = input("Continue to next dataset? [y/n]: ").strip().lower()
                if response in ['y', 'n']:
                    if response == 'n':
                        logger.info("Analysis stopped by user")
                        return
                    break
            
            dataset_results[dataset_id] = dataset_config
        
        # Save results
        self._save_results(dataset_results)
    
    def _save_results(self, dataset_results):
        """Save all configs and results to iteration folder"""
        logger.info(f"\n{'='*80}")
        logger.info("SAVING RESULTS")
        logger.info(f"{'='*80}\n")
        
        # Save config files
        config_copy = dict(self.config)
        config_copy['datasets'] = list(dataset_results.values())
        
        with open(self.results_dir / 'config.yaml', 'w') as f:
            yaml.dump(config_copy, f, default_flow_style=False)
        logger.info(f"✓ Saved config.yaml")
        
        with open(self.results_dir / 'camera_settings.yaml', 'w') as f:
            yaml.dump(self.camera_settings, f, default_flow_style=False)
        logger.info(f"✓ Saved camera_settings.yaml")
        
        with open(self.results_dir / 'defect_config.yaml', 'w') as f:
            yaml.dump(self.defect_config, f, default_flow_style=False)
        logger.info(f"✓ Saved defect_config.yaml")
        
        # Save analysis metadata
        metadata = {
            'iteration': self.iteration,
            'timestamp': datetime.now().isoformat(),
            'user': getpass.getuser(),
            'num_datasets': len(dataset_results),
            'datasets_analyzed': list(dataset_results.keys()),
        }
        with open(self.results_dir / 'metadata.json', 'w') as f:
            json.dump(metadata, f, indent=2)
        logger.info(f"✓ Saved metadata.json")
        
        logger.info(f"\nAll results saved to: {self.results_dir}")


