#!/usr/bin/env python3
"""
Interactive KZ Article Analysis Script

Workflow:
1. Load config files (main config, camera settings, defect config)
2. For each dataset:
   a. Load raw data from specified day/sequences
   b. Show 5 random profiles from different detunings (KZ_det_scan variation)
   c. Ask user to tune affine_correction, x_min_integration, x_max_integration
   d. Run waterfall_v2 analysis with KZ_det_scan mode
   e. Show domain wall plots
   f. Ask user to set domain_balance_fit_window
   g. Ask for permission to continue to next dataset
3. Save iteration folder with all configs and analysis results

Usage:
    python run_kz_article_analysis.py /path/to/config.yaml [--iteration N]
"""

